# --- ダメージ計算ロジック（兵刃:武勇依存 / 計略:知略依存） ---
def calc_damage(stat_value, def_power, dmg_rate):
    if dmg_rate == 0:
        return 0
    def_mitigation = 100.0 / (100.0 + def_power)
    effective_atk = stat_value * dmg_rate
    return int(effective_atk * def_mitigation * 10)

