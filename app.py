def get_character_traits(character_name):
    """指定した武将が所持している特性の一覧を取得する"""
    owned = []
    for trait_name, data in TRAIT_DATABASE.items():
        if character_name in data["characters"]:
            owned.append({
                "trait": trait_name,
                "type": data["type"],
                "rank": data["characters"][character_name],
                "effect": data["effect"]
            })
    return owned


